"""Convert the existing DINO fine-tuning dataset into YOLO detection format.

Reuses the *exact* frames, GT boxes, and train/val/test splits that
`dino_fine_tuning/src/build_dataset.py` materialized -- so YOLO is trained and
evaluated on identical geometry to DINO (apples-to-apples). No FFT recompute.

Reads   <dino-dataset>/frames_{split}.npy  (uint8 N x rows x nfft, grayscale dB)
        <dino-dataset>/frames.csv          (mem_pos -> frame_id, is_signal, split)
        <dino-dataset>/regions.csv         (per-box row0/row1/col0/col1 in frame px)
Writes  <out>/images/{split}/<frame_id>.png
        <out>/labels/{split}/<frame_id>.txt   ("0 cx cy w h" normalized; empty = background)
        yolo_training/configs/dataset.yaml

Binary task: single class 0 = signal (noise frames get an empty label file = background).
"""
from __future__ import annotations
import argparse, csv, json
from collections import defaultdict
from pathlib import Path
import numpy as np
from PIL import Image

FT = Path("/home/bqn82/Holohub-Signal-Detection/dino_fine_tuning")
YT = Path("/home/bqn82/Holohub-Signal-Detection/yolo_training")


def log(m): print(f"[yolo-ds] {m}", flush=True)


def load_regions(regions_csv: Path):
    by_frame = defaultdict(list)
    with open(regions_csv) as f:
        for r in csv.DictReader(f):
            by_frame[r["frame_id"]].append(r)
    return by_frame


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--dino-dataset", default=str(FT / "data/dataset"))
    ap.add_argument("--out", default=str(YT / "data/rf_spectrograms"))
    ap.add_argument("--splits", default="train,val,test")
    args = ap.parse_args()

    ds = Path(args.dino_dataset); out = Path(args.out)
    meta = json.loads((ds / "dataset_meta.json").read_text())
    nfft, rows = int(meta["nfft"]), int(meta["frame_rows"])
    log(f"frame geometry: {rows} rows x {nfft} cols (nfft={nfft})")

    by_frame = load_regions(ds / "regions.csv")
    # frames.csv -> per split: list of (mem_pos, frame_id, is_signal)
    frames_by_split = defaultdict(list)
    with open(ds / "frames.csv") as f:
        for r in csv.DictReader(f):
            frames_by_split[r["split"]].append((int(r["mem_pos"]), r["frame_id"], int(r["is_signal"])))

    counts = {}
    for sp in args.splits.split(","):
        arr_path = ds / f"frames_{sp}.npy"
        if not arr_path.exists():
            log(f"skip {sp} (no {arr_path.name})"); continue
        frames = np.load(arr_path, mmap_mode="r")
        img_dir = out / "images" / sp; lbl_dir = out / "labels" / sp
        img_dir.mkdir(parents=True, exist_ok=True); lbl_dir.mkdir(parents=True, exist_ok=True)
        n_img = n_box = n_bg = 0
        for mem_pos, frame_id, is_sig in frames_by_split[sp]:
            safe = frame_id.replace("#", "_")
            Image.fromarray(np.asarray(frames[mem_pos]), mode="L").save(img_dir / f"{safe}.png")
            lines = []
            for b in by_frame.get(frame_id, []):
                r0, r1 = float(b["row0"]), float(b["row1"])
                c0, c1 = float(b["col0"]), float(b["col1"])
                cx = ((c0 + c1) / 2) / nfft; cy = ((r0 + r1) / 2) / rows
                w = (c1 - c0) / nfft; h = (r1 - r0) / rows
                if w <= 0 or h <= 0:
                    continue
                # clamp to [0,1]
                cx, cy = min(max(cx, 0.0), 1.0), min(max(cy, 0.0), 1.0)
                w, h = min(w, 1.0), min(h, 1.0)
                lines.append(f"0 {cx:.6f} {cy:.6f} {w:.6f} {h:.6f}")
            (lbl_dir / f"{safe}.txt").write_text("\n".join(lines) + ("\n" if lines else ""))
            n_img += 1; n_box += len(lines); n_bg += (0 if lines else 1)
        counts[sp] = {"images": n_img, "boxes": n_box, "background_frames": n_bg}
        log(f"{sp}: {n_img} images, {n_box} boxes, {n_bg} background(noise) frames")

    # dataset.yaml
    yml = YT / "configs/dataset.yaml"
    yml.write_text(
        "# Auto-generated by src/build_yolo_dataset.py from the DINO dataset (same frames/boxes/splits).\n"
        f"path: {out}\n"
        "train: images/train\n"
        "val: images/val\n"
        "test: images/test\n"
        "names:\n  0: signal\n")
    log(f"wrote {yml}")
    (out / "build_meta.json").write_text(json.dumps(
        {"source": str(ds), "nfft": nfft, "frame_rows": rows, "counts": counts}, indent=2))
    log(f"DONE -> {out}  counts={counts}")


if __name__ == "__main__":
    main()
