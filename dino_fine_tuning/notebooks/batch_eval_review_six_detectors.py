# %% [markdown]
# ## Caveats (read before quoting numbers)
#
# 1. **Metrics are identical across detectors** — all scored by `eval_detector_masks.py`
#    over `sweep_20260630`; only the masks differ. Deployed detectors (coherent_power,
#    cuda_dino, power_detection, computer_vision) are the container's own outputs, unmodified.
# 2. **GT includes buried signals** — annotation boxes mark where a transmitter was, even below
#    the noise floor at high attenuation; pixel recall/IoU is therefore low for *all* detectors there.
# 3. **Pixel-IoU favors the fine-tuned model** (trained to reproduce the filled-box convention);
#    the sparse energy masks of the traditional detectors are penalized on IoU even when they localize
#    the signal. **Region detection rate is the fair headline.**
# 4. **Geometry** — batch frames are 512×10240 (nfft=10240); the fine-tuned model runs at nfft=1024
#    and its mask is MAX-pooled onto the display grid (preserves thin Zadoff-Chu pulses). The two new
#    traditional detectors emit at the batch grid and are scored/resized identically to coherent_power.
# 5. **Power Detection consumes raw IQ** (taps the offline source and runs its own FFT); Computer Vision
#    consumes the spectrogram like the DINO / coherent detectors. Neither is tuned per file (statistical/
#    adaptive thresholds only).
# 6. `DET_THRESHOLD` matches the original notebook (0.1). Rebuild tables via
#    `src/assemble_six_detectors.py` (see the setup cell + `SIX_DETECTOR_WORKFLOW.md`).
